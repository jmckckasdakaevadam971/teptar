--  тысячи ветвей, расходящихся в источниках. Здесь даны лишь отдельные
--  примеры структуры; основной массив пополняется администраторами тейпов
--  через интерфейс приложения.
-- ============================================================================
INSERT INTO gars (teip_id, name, description) VALUES
    ((SELECT id FROM teips WHERE name='Беной'),    'Алдамгӏар',   'Ветвь тейпа Беной'),
    ((SELECT id FROM teips WHERE name='Аллерой'),  'Юсуп-некъи',  'Пример ветви (уточняется)'),
    ((SELECT id FROM teips WHERE name='Курчалой'), 'Хьаси-некъи', 'Пример ветви (уточняется)')
ON CONFLICT (teip_id, name) DO NOTHING;

-- ============================================================================
--  9. ДОПОЛНИТЕЛЬНЫЕ НЕКЪИ (под-ветви)
--  Также приведены как пример структуры; основной массив пополняется
--  пользователями через интерфейс.
-- ============================================================================
INSERT INTO nekyi (gar_id, name, description) VALUES
    ((SELECT g.id FROM gars g JOIN teips t ON t.id=g.teip_id
      WHERE t.name='Беной' AND g.name='Алдамгӏар'), 'Дада-некъи', 'Под-ветвь (пример)')
ON CONFLICT (gar_id, name) DO NOTHING;

